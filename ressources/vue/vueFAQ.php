				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse1">
					  Qu'est-ce que lepetitbal ?</a>
					</h4>
				  </div>
				  <div id="collapse1" class="panel-collapse collapse in">
					<div class="panel-body">
						<b>lepetitbal</b> est un annuaire interactif et gratuit qui aide l'utilisateur à trouver les cours de danse, les pratiques et soirées dansantes ainsi que tous les évènements dansants autour de Grenoble</br>
						<b>lepetitbal</b> s'engage à proposer un contenu riche et à jour afin de vous fournir des informations pertinentes.</br>
					</div>
				  </div>
				</div>
				<div class="panel panel-default">
				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse2">
					  Comment ça fonctionne?</a>
					</h4>
				  </div>
				  <div id="collapse2" class="panel-collapse collapse">
					<div class="panel-body">
						<b>lepetitbal</b> fonctionne grâce au recensement communautaire.</br>
						Les membres ont la possibilité de recenser les lieux ou les évènements liés à la danse sociale partout dans le monde pour en faire profiter la communauté.</br>
						Ils peuvent modifier directement les informations des annonces sauf pour les lieux ayant un propriétaire.</br>
						Ils peuvent également recommander un lieu ou laisser un commentaire sur la fiche du lieu.</br>
					</div>
				  </div>
				</div>
				<div class="panel panel-default">
				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse3">
					  C'est gratuit?</a>
					</h4>
				  </div>
				  <div id="collapse3" class="panel-collapse collapse">
					<div class="panel-body">
						Oui. L'accès au site internet, la recherche d'information, l'inscription en tant que membre ou propriétaire d'un lieu et les différentes fonctionnalités proposées sont entièrement gratuits.</br>
						Seules les fonctionnalités exclusives tels que la promotion d'un lieu et l'ajout d'évènements pour les annonceurs nécessitent l'utilisation de jetons.</br>
						Il est possible d'acheter des jetons.</br>
					</div>
				  </div>
				</div>
				<div class="panel panel-default">
				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse4">
					  Est-ce que je peux revendiquer un lieu qui m'appartient?</a>
					</h4>
				  </div>
				  <div id="collapse4" class="panel-collapse collapse">
					<div class="panel-body">
						Oui. Vous pouvez revendiquer un lieu en nous envoyant un mail ou alors en cliquant sur le bouton "Signaler" disponible sur le billet.</br>
						Cela vous permet d'obtenir l'exclusivité sur la modification et la diffusion d'information relatif à votre lieu. Les autres membres ne pourront plus modifier votre lieu.</br>
					</div>
				  </div>
				</div>	
				<div class="panel panel-default">
				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse5">
					  A quoi servent les jetons?</a>
					</h4>
				  </div>
				  <div id="collapse5" class="panel-collapse collapse">
					<div class="panel-body">
						Les jetons permettent aux annonceur de:</br>
							- Promouvoir un ou plusieurs lieux</br>
							- Diffuser des évènements</br>
						La promotion d'un lieu permet aux annonceurs de mettre en avant leur annonce.</br>
						La diffusion d'évènement permet de faire apparaitre des évènement dans le calendrier des évènement.</br>
						Les jetons sont utilisables lorsque vous le souhaitez. Vous n'êtes pas obligé de dépenser tous vos jetons d'un coup!</br>
					</div>
				  </div>
				</div>
				<div class="panel panel-default">
				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse6">
					  Quels sont les avantages de la promotion d'un lieu?</a>
					</h4>
				  </div>
				  <div id="collapse6" class="panel-collapse collapse">
					<div class="panel-body">
						La promotion d'un lieu permet aux annonceurs de mettre en avant leur annonce.</br>
						Cela est particulièrement utile pour les annonceurs de cours de danse à l'année</br>
						Un lieu promu aura en plus d'un lieu normal:</br>
						++++++ Une visibilité grandement améliorée ++++++</br>
							- Placement automatique en tête des résultats de recherche standard même après filtre</br>
							- Changement de couleur du lieux, de orange à rouge dans les zones de recherche standard et calendrier</br>
						++++++ Un espace publicitaire plus grand ++++++</br>
							- Ajout d'une photo de présentation</br>
							- Ajout d'un espace de présentation du lieu</br>
							- Ajout d'un lien direct avec un site internet externe ou une page facebook</br>
					</div>
				  </div>
				</div>
				<div class="panel panel-default">
				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse7">
						Comment promouvoir un lieu en utilisant des jetons?</a>
					</h4>
				  </div>
				  <div id="collapse7" class="panel-collapse collapse">
					<div class="panel-body">
						La promotion d'un lieu s'effectue uniquement sur la fiche du lieu dans la section "Promotion du lieu".</br>
						Il vous suffit d'indiquer une date de fin de promotion ou alors de sélectionner le nombre de jetons que vous voulez utiliser.</br>
						La promotion du lieu s'effectue immédiatement.</br>
						Vous pouvez par la suite accéder aux fonctionnalités exclusives pour ce lieu.</br>
					</div>
				  </div>
				</div>
				<div class="panel panel-default">
				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse8">
					  C'est quoi le calendrier des évènements?</a>
					</h4>
				  </div>
				  <div id="collapse8" class="panel-collapse collapse">
					<div class="panel-body">
						Le calendrier des évènements est un espace d'information regroupant tous les évènements pour les 30 prochains jours autour de l'adresse renseignée et pour le style de danse recherché.</br>
						Cet espace est disponible par tous les internautes inscrits ou non inscrits en cliquant sur le bouton "Calendrier".</br>
						Les évènements sont postés directement par les annonceurs en utilisant des jetons.</br>
					</div>
				  </div>
				</div>
				<div class="panel panel-default">
				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse9">
					  Combien ça coute de diffuser des évènements et de promouvoir un lieu?</a>
					</h4>
				  </div>
				  <div id="collapse9" class="panel-collapse collapse">
					<div class="panel-body">
						lepetitbal utilise un système de jetons pour réaliser ces fonctions exclusives.</br>
						La promotion d'un lieu consomme 1 jeton par jour.</br>
						L'ajout d'un évènement coûte 5 jetons.</br>
					</div>
				  </div>
				</div>
				<div class="panel panel-default">
				  <div class="panel-heading">
					<h4 class="panel-title">
					  <a data-toggle="collapse" data-parent="#accordion" href="#collapse10">
					  Combient coûte les jetons et comment en obtenir?</a>
					</h4>
				  </div>
				  <div id="collapse10" class="panel-collapse collapse">
					<div class="panel-body">
						Pour connaître la grille tarifaire des jetons et pour en obtenir, vous devez vous connecter à votre espace Membre et aller dans la section "Achat Jetons".</br>
						Après avoir sélectionné le pack, vous pouvez modifier la quantité directement sur le site de PayPal.</br>
						Le paiement pour l'obtention des jetons se fait exclusivement via le site PayPal.</br>
						Vous pouvez utiliser ces jetons immédiatement après vérification de l'achat par le service Paypal.</br></p>
						
						Vous pouvez regarder le nombre de jetons qu'il vous reste sur le compte dans votre espace Membre dans la section "Mon Compte".</br>
